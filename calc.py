x = float(input("Enter x: ")) #Темир Саксыков 
y = float(input("Enter y: ")) #Ahmeev Ais
opr = input("Enter operation: ") #Джилеева Сяхля

if opr == '+': #Nominhan Budenkaev
	print(x + y) 
elif opr == '-': 
	print(x - y)
elif opr == '*': #Temir
	print(x * y)
elif opr == '/': #Kamila
	print(x / y)
else: #Aldar
	print("Incorrect option")
